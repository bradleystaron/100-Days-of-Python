# This prints out the first character of the string - called Subscripting
print("Hello"[-1])

# String Concatenation - will print out 123345
print("123" + "345")

# This is representative of the integer data type
print(123 + 345)

# Float data types have decimal places
print(123.345)

# Booleans are either true or false
print(True)
print(False)